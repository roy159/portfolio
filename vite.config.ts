import { defineConfig } from 'vite'
import path from 'path'
import tailwindcss from '@tailwindcss/vite'
import react from '@vitejs/plugin-react'


function figmaAssetResolver() {
  return {
    name: 'figma-asset-resolver',
    resolveId(id) {
      if (id.startsWith('figma:asset/')) {
        const filename = id.replace('figma:asset/', '')
        return path.resolve(__dirname, 'src/assets', filename)
      }
    },
  }
}

export default defineConfig({
  // IMPORTANT: Set base path based on your repository name
  //
  // GitHub Pages:
  // - If repo is "username.github.io" → use: base: '/'
  // - If repo is "portfolio" → use: base: '/portfolio/'
  // - If repo is "my-site" → use: base: '/my-site/'
  //
  // The base MUST match your repo name exactly (case-sensitive)
  base: '/portfolio/', // ← CHANGE THIS to match YOUR GitHub repo name!

  plugins: [
    figmaAssetResolver(),
    // The React and Tailwind plugins are both required for Make, even if
    // Tailwind is not being actively used – do not remove them
    react(),
    tailwindcss(),
  ],
  resolve: {
    alias: {
      // Alias @ to the src directory
      '@': path.resolve(__dirname, './src'),
    },
  },

  // File types to support raw imports. Never add .css, .tsx, or .ts files to this.
  assetsInclude: ['**/*.svg', '**/*.csv'],
})
